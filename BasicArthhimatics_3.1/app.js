
/*
Example Addition, Subtraction, multiplication operators
*/
var x = 5 ;
var y = 10 ;
// var z = 15 ;
 
//Addition
p = x + y;
document.write(" x + y is ", p);
document.write("<br />");

//Subtraction
q = y - x;
document.write(" x - y is ", q);
document.write("<br />");

//Multiplication
r = x * y;
document.write(" x * y is ", r);
document.write("<br />");

//Division
s = y / x;
document.write(" x / y is ", s);
document.write("<br />");

//Modulus
t = x % 2;
document.write(" x % 2 is ", t);
document.write("<br />");

//Increment
x++;
document.write(" x++ is ", x);
document.write("<br />"); 

//Decrement
y--;
document.write("y-- is ", y);
